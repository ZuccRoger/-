/**
 * @param {string[]} words
 * @param {*[]} chars
 * @return {number}
 */
var countCharacters = function (words, chars) {
    let totalLength=0,flag=false,chars_raw=chars
    words.forEach((item)=>{
        for(let i=0;i<item.length;i++){
            if(chars.indexOf(item[i])==-1){
                flag=true
            }else {
                chars=chars.replace(item[i],0)
            }
        }
        if(flag==false){
            totalLength+=item.length
        }
        flag=false
        chars=chars_raw
    })
    return totalLength
};
